export type NavItem = {
  label: string;
  href: string;
  icon: React.ReactNode;
};

const ADMIN_LINKS: NavItem[] = [{ label: "Usuarios", href: "/dashboard/admin/users", icon: <UsuariosIcon /> }];
const LINK_SERVICE_LINK: NavItem = { label: "LinkService", href: "/dashboard/linkservice", icon: <LinkServiceIcon /> };
const APPOINTMENTS_LINK: NavItem = { label: "Citas", href: "/dashboard/appointments", icon: <CitasIcon /> };
const INVOICING_LINK: NavItem = { label: "Facturacion", href: "/dashboard/invoicing", icon: <FacturacionIcon /> };

function buildSummaryLabel(): string {
  return "Resumen";
}

export function getDashboardNavItems({
  showAdmin = false,
  enableLinkService = false,
  enableInvoice = false,
  enableInvoicing = false,
  activeModuleLabels = []
}: {
  showAdmin?: boolean;
  enableLinkService?: boolean;
  enableInvoice?: boolean;
  enableInvoicing?: boolean;
  activeModuleLabels?: string[];
}): NavItem[] {
  const summary: NavItem = {
    label: buildSummaryLabel(),
    href: "/dashboard",
    icon: <ResumenIcon />
  };

  const base: NavItem[] = [
    summary,
    { label: "Modulos", href: "/dashboard/modules", icon: <ModulosIcon /> },
    { label: "Configuracion", href: "/dashboard/settings", icon: <ConfiguracionIcon /> }
  ];

  return [
    ...base,
    ...(enableLinkService ? [LINK_SERVICE_LINK] : []),
    ...(enableInvoice ? [APPOINTMENTS_LINK] : []),
    ...(enableInvoicing ? [INVOICING_LINK] : []),
    ...(showAdmin ? ADMIN_LINKS : [])
  ];
}
